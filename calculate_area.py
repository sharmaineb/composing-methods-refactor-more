def find_maximum_value(lst):
    """Find and return the maximum value in the given list."""
    max_value = lst[0]
    for value in lst:
        if value > max_value:
            max_value = value
    return max_value

def extract_words_from_sentence(sentence):
    """Extract and return a list of words from the input sentence."""
    words = sentence[0:].split(' ')
    return words

